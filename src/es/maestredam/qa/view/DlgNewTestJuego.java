    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        seguir = true;
        int extraida;
        contador++;
        if (pregunta.getRespuestaB()==null){
            if (pregunta.corregirCorta(txtRespuesta.getText(), pregunta) == true){
                puntuacion = Integer.parseInt(txtPuntuacion.getText());
                puntuacion++;
                txtPuntuacion.setText(" "+puntuacion);
            }
        }
        else{
            if (radioBtn1.isSelected()){
                if (pregunta.corregirTest(radioBtn1.getText(), pregunta)){
                    puntuacion = Integer.parseInt(txtPuntuacion.getText());
                    puntuacion+=1;
                    txtPuntuacion.setText(" "+puntuacion);
                }
            }
            else if (radioBtn2.isSelected()){
                if (pregunta.corregirTest(radioBtn2.getText(), pregunta)){
                    puntuacion = Integer.parseInt(txtPuntuacion.getText());
                    puntuacion+=1;
                    txtPuntuacion.setText(" "+puntuacion);
                }
            }
            else if (radioBtn3.isSelected()){
                if (pregunta.corregirTest(radioBtn3.getText(), pregunta)){
                    puntuacion = Integer.parseInt(txtPuntuacion.getText());
                    puntuacion+=1;
                    txtPuntuacion.setText(" "+puntuacion);
                }
            }
        }
        extraida = (pregunta.getCuentaExt())+1;
        gestor.guardarListaPreguntas(preguntas, false);
        getPregunta();
    }//GEN-LAST:event_btnAceptarActionPerformed

