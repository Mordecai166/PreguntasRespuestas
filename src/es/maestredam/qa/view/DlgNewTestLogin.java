    private void btnJugarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnJugarActionPerformed
        clicAceptar = true;
        User aux = new User(txtNombre.getText(), txtClave.getText());
        GestorInfo gestor = new GestorInfo(FileSystemView.getFileSystemView().getDefaultDirectory().getPath()+"/ProyectoPreguntasRespuestas/usuarios.dam");
        LinkedList<User> usuarios = gestor.leerUsuarios();
        boolean encontrado = false;
        for (int i=0; i<usuarios.size() && !encontrado; i++){
            usuario = usuarios.get(i);
            if (usuario.getNombre().equals(aux.getNombre()) && usuario.getPassword().equals(aux.getPassword())){
                encontrado = true;
            }
        }
        if (encontrado == false){
            lblError.setVisible(true);
            txtNombre.setText("");
            txtClave.setText("");
        }
        else{
            this.dispose();
        }
    }//GEN-LAST:event_btnJugarActionPerformed
    
